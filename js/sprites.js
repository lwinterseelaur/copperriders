export function makeRect(scene, key, w, h, color) {
  if (scene.textures.exists(key)) return;
  const c = document.createElement('canvas');
  c.width = w;
  c.height = h;
  const ctx = c.getContext('2d');
  ctx.fillStyle = color;
  ctx.fillRect(0, 0, w, h);
  scene.textures.addCanvas(key, c);
}

export function makeSky(scene, key, w, h, top, bottom) {
  if (scene.textures.exists(key)) return;
  const c = document.createElement('canvas');
  c.width = w;
  c.height = h;
  const ctx = c.getContext('2d');
  const grd = ctx.createLinearGradient(0, 0, 0, h);
  grd.addColorStop(0, top);
  grd.addColorStop(1, bottom);
  ctx.fillStyle = grd;
  ctx.fillRect(0, 0, w, h);
  scene.textures.addCanvas(key, c);
}

export function makeGround(scene, key, w, h, soil, grass, accent) {
  if (scene.textures.exists(key)) return;
  const c = document.createElement('canvas');
  c.width = w;
  c.height = h;
  const ctx = c.getContext('2d');
  ctx.fillStyle = soil;
  ctx.fillRect(0, 0, w, h);
  // metal plate edge
  ctx.fillStyle = grass;
  ctx.fillRect(0, 0, w, 3);
  ctx.fillStyle = accent;
  for (let i = 0; i < w; i += 4) {
    ctx.fillRect(i, 1, 1, 1);
    ctx.fillRect(i + 2, 2, 1, 1);
  }
  ctx.fillStyle = '#000';
  ctx.globalAlpha = 0.22;
  for (let i = 0; i < 40; i++) {
    const x = Math.floor(Math.random() * w);
    const y = 4 + Math.floor(Math.random() * (h - 4));
    ctx.fillRect(x, y, 1, 1);
  }
  ctx.fillStyle = '#b56a3a';
  ctx.globalAlpha = 0.4;
  for (let i = 0; i < 12; i++) {
    const x = Math.floor(Math.random() * w);
    const y = 5 + Math.floor(Math.random() * (h - 5));
    ctx.fillRect(x, y, 1, 1);
  }
  // Aurubis blue accent stripe (subtle)
  ctx.fillStyle = '#0076A7';
  ctx.globalAlpha = 0.5;
  for (let i = 0; i < 4; i++) {
    const x = Math.floor(Math.random() * w);
    const y = 4 + Math.floor(Math.random() * (h - 4));
    ctx.fillRect(x, y, 2, 1);
  }
  ctx.globalAlpha = 1;
  scene.textures.addCanvas(key, c);
}

export function registerSprites(scene) {
  Object.entries(SPRITES).forEach(([key, str]) => {
    if (scene.textures.exists(key)) return;
    const canvas = rasterize(str);
    scene.textures.addCanvas(key, canvas);
  });
}

