function rasterize(rows, palette = PAL) {
  const cleaned = rows.replace(/^\n+|\n+$/g, '').split('\n');
  const w = Math.max(...cleaned.map(r => r.length));
  const h = cleaned.length;
  const canvas = document.createElement('canvas');
  canvas.width = w;
  canvas.height = h;
  const ctx = canvas.getContext('2d');
  for (let y = 0; y < h; y++) {
    const row = cleaned[y];
    for (let x = 0; x < row.length; x++) {
      const ch = row[x];
      if (ch === '.' || ch === ' ') continue;
      const color = palette[ch];
      if (!color) continue;
      ctx.fillStyle = color;
      ctx.fillRect(x, y, 1, 1);
    }
  }
  return canvas;
}

